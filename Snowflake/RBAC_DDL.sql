-- Audit log table for RBAC changes
CREATE TABLE IF NOT EXISTS GOVERNANCE_DB.AUDIT.RBAC_AUDIT_LOG (
    audit_id        VARCHAR(255) DEFAULT UUID_STRING() PRIMARY KEY,
    action_type     VARCHAR(50)  NOT NULL,     -- GRANT, REVOKE, ROLE_CREATE, ROLE_DROP
    target_role     VARCHAR(255),
    target_user     VARCHAR(255),
    target_resource VARCHAR(500),
    privilege       VARCHAR(255),
    performed_by    VARCHAR(255) NOT NULL,
    performed_at    TIMESTAMP_NTZ DEFAULT CURRENT_TIMESTAMP(),
    details         VARIANT
);

-- Role assignment tracking
CREATE TABLE IF NOT EXISTS GOVERNANCE_DB.AUDIT.ROLE_ASSIGNMENTS (
    assignment_id   VARCHAR(255) DEFAULT UUID_STRING() PRIMARY KEY,
    user_name       VARCHAR(255) NOT NULL,
    role_name       VARCHAR(255) NOT NULL,
    assigned_by     VARCHAR(255) NOT NULL,
    assigned_at     TIMESTAMP_NTZ DEFAULT CURRENT_TIMESTAMP(),
    revoked_by      VARCHAR(255),
    revoked_at      TIMESTAMP_NTZ,
    is_active       BOOLEAN DEFAULT TRUE,
    expires_at      TIMESTAMP_NTZ,
    environment     VARCHAR(50)  -- DEV, QA, PROD
);

-- Agent task scope tracking
CREATE TABLE IF NOT EXISTS GOVERNANCE_DB.AUDIT.AGENT_TASK_LOG (
    task_id         VARCHAR(255) DEFAULT UUID_STRING() PRIMARY KEY,
    agent_id        VARCHAR(255) NOT NULL,
    task_type       VARCHAR(100) NOT NULL,
    target_schema   VARCHAR(500),
    action          VARCHAR(100) NOT NULL,
    query_hash      VARCHAR(255),
    rows_affected   INT,
    status          VARCHAR(50)  NOT NULL,     -- RUNNING, COMPLETED, FAILED, DENIED
    started_at      TIMESTAMP_NTZ DEFAULT CURRENT_TIMESTAMP(),
    completed_at    TIMESTAMP_NTZ,
    error_message   VARCHAR(2000),
    performed_by    VARCHAR(255) NOT NULL
);

