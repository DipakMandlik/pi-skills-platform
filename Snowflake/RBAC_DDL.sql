CREATE MASKING POLICY IF NOT EXISTS pii_email_mask
  AS (val STRING) RETURNS STRING ->
  CASE
    WHEN CURRENT_ROLE() IN ('ORG_ADMIN', 'SECURITY_ADMIN') THEN val
    ELSE '***MASKED***'
  END;

CREATE MASKING POLICY IF NOT EXISTS pii_phone_mask
  AS (val STRING) RETURNS STRING ->
  CASE
    WHEN CURRENT_ROLE() IN ('ORG_ADMIN', 'SECURITY_ADMIN') THEN val
    ELSE REGEXP_REPLACE(val, '[0-9]', 'X')
  END;

CREATE MASKING POLICY IF NOT EXISTS pii_ssn_mask
  AS (val STRING) RETURNS STRING ->
  CASE
    WHEN CURRENT_ROLE() IN ('ORG_ADMIN', 'SECURITY_ADMIN') THEN val
    ELSE 'XXX-XX-' || RIGHT(val, 4)
  END;

CREATE MASKING POLICY IF NOT EXISTS pii_name_mask
  AS (val STRING) RETURNS STRING ->
  CASE
    WHEN CURRENT_ROLE() IN ('ORG_ADMIN', 'SECURITY_ADMIN', 'DATA_ENGINEER', 'ANALYTICS_ENGINEER') THEN val
    ELSE LEFT(val, 1) || '****'
  END;

